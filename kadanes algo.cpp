// kadanes algorithm O(n);

#include<bits/stdc++.h>
using namespace std;
int main(){
	int n;
	cin>>n;
	int a[n];
	for(int i=0;i<n;i++){
		cin>>a[i];
	}
	
	int dp[n];
	dp[0]=a[0];
	int tot_max=INT_MIN;
	for(int i=1;i<n;i++){
		dp[i]=max(a[i],dp[i-1]+a[i]);
		tot_max=max(dp[i],tot_max);
	}
	
	cout<<tot_max<<endl;
}

